Lemma and_example2'' : forall n m : nat, n = 0 -> m = 0 -> n + m = 0.

Proof.
(* The goal is to prove that if n is 0 and m is 0, then n + m is 0.
We will do this by applying the and_intro lemma, which states that
     if A and B are propositions and we have proofs of A and B, then we can
     construct a proof of A /\ B.
*)
  intros n m Hn Hm.
(* We now need to apply the and_intro lemma with A = n = 0 and B = m = 0.
We have proofs of these propositions (Hn and Hm), so we can apply and_intro.
*)
  apply (@zero_add_zero_0 n m Hn Hm).
Qed.